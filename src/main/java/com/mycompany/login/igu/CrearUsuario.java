    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        /* Traemos cada uno de los valores con los metodos .getText, para el comobo box,
            utilizamos .getSelectItem y lo paseamos para que traiga el item seleccionado*/
        
        String usuario = txtUsuario.getText();
        String contra = txtContra.getText();
        String rol = (String)cmbRol.getSelectedItem();
        
        if (!txtUsuario.getText().trim().equals("") && !txtContra.getText().trim().equals("")) {
             
            
            /*Llamamos la controladora y el metodo crear usuario, con los parametros de
         usuario, contrase√±a y rol*/
        
        control.crearUsuario(usuario,contra,rol);
        mostrarMensaje("Creado correctamente!!!", "Info", "Creacion exitosa");
        this.dispose();
            
            
            
        } else {
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

